import UIKit
import SnapKit
import DGCharts

final class WeekStepView: BaseView {
    let chartView = StepChartView()
    
    override func configureHierarchy() {
        addSubview(chartView)
    }
    
    override func configureLayout() {
        chartView.snp.makeConstraints { make in
            make.edges.equalTo(safeAreaLayoutGuide)
        }
    }
}

class ChartMarker: MarkerView {
    let label = UILabel()
    
    override init(frame: CGRect) {
        super.init(frame: frame)
        
        self.offset.x = -self.frame.size.width / 2.0
        self.offset.y = -self.frame.size.height - 10

        addSubview(label)
        label.snp.makeConstraints { make in
            make.edges.equalTo(self)
        }
        
        label.backgroundColor = .secondaryColor
        label.layer.cornerRadius = 12
        label.clipsToBounds = true
        label.textAlignment = .center
        label.font = .boldCallout
        label.textColor = .titleColor
    }
    
    required init?(coder: NSCoder) {
        fatalError("init(coder:) has not been implemented")
    }
    
    public override func refreshContent(entry: ChartDataEntry, highlight: Highlight) {
        label.text = "\(Int(round(entry.y)).numberToDecimal())"
        layoutIfNeeded()
    }
}

